#! /usr/bin/usr python3

# incrementing
counter = 0
print("Incrementing starting point: " + str(counter))
counter = counter + 1
print("counter =" + str(counter))
counter += 1
print("counter = " + str(counter))

# Decrementing
counter = 10
print("Decrementing starting point: " + str(counter))
counter -= 1
print("counter = " + str(counter))
counter -= 1
print("counter = " + str(counter))

