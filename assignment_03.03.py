#! /usr/bin/usr python3

# basic arithmetic operators

print(7 + 3)
print(7 - 3)
print(7 * 3)
print(7 / 3)

# Two operands
print(7 // 3)
print(7 ** 3)

# Modulo operator
print(7 % 3)

# Order of precedence (Default)
# Multipication and division will happen first
print(7 + 3 * 5)

# User specified order
print((7 + 3) * 5)